/**
 * Retorna todos os unitIds únicos necessários para uma fase
 * @param {string} phase — ex: "F1", "F3"
 * @returns {string[]}
 */
export function getRequiredUnitsForPhase(phase) {
  const ids = new Set();
  Object.keys(platoonRequirements)
    .filter(k => k.startsWith(phase + '_'))
    .forEach(key =>
      Object.values(platoonRequirements[key]).forEach(slots =>
        slots.forEach(s => ids.add(s.unitId))
      )
    );
  return [...ids];
}

/**
 * Encontra em qual operação/slot um personagem aparece num planeta
 * @param {string} planetKey — ex: "F1_DS"
 * @param {string} unitId
 * @returns {{ op: number, slot: number }[]}
 */
export function findUnitInPlanet(planetKey, unitId) {
  const planet = platoonRequirements[planetKey];
  if (!planet) return [];
  const results = [];
  Object.entries(planet).forEach(([op, slots]) =>
    slots.forEach(s => { if (s.unitId === unitId) results.push({ op: parseInt(op), slot: s.slot }); })
  );
  return results;
}

